#include "window.hh"

using namespace jones::windows;

void window::set_last_drawn(int start_y, int start_x) {
  start_y_ = start_y;
  start_x_ = start_x;
}

int window::get_last_start_y() {
  return start_y_;
}

int window::get_last_start_x() {
  return start_x_;
}
